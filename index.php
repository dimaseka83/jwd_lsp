<script>
  $(document).ready(function () {
      let hasilresponse = [];
      const implementkegiatan = (data) => {
        // ul li #portfolio-flters
        let html = '';
        html += `
          <li data-filter="*" class="filter-active">Semua</li>
        `;
        data.forEach((item, index) => {
          html += `
            <li data-filter=".filter-${item.id}">${item.nama}</li>
          `;
        });
        $('#portfolio-flters').html(html);
      }

      $.ajax({
        type: "GET",
        url: "./admin/kegiatan/routekegiatan.php",
        success: function (response) {
          implementkegiatan(response);
          hasilresponse = response;
        }
      });

      // click class li on ul #portfolio-flters
      $(document).on('click', '#portfolio-flters li', function () {
        let filter = $(this).data('filter');
        let desc = $('.portfolio-flters-description');
        if (filter == '*') {
          desc.text('Kegiatan Pelatihan dan Sertifikasi Profesi di bidang Komunikasi dan Informatika yang diselenggarakan oleh BPSDMP Surabaya');
        } else {
          let data = hasilresponse.filter((item, index) => {
            return item.id == filter.split('-')[1];
          });
          desc.text(data[0].deskripsi);
        }
      });
  });
</script>
